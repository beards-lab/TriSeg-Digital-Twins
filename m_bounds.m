function [ub, lb] = m_bounds(mods)
%% Function Purpose:
% A simple function to set up boundary conditions for the optimizer.

% Created by Andrew Meyer
% Last modified: 10/29/2024
ub = zeros(1,length(mods));
lb = ub;
pairs = dictionary('C_SA', {[0.1 10]}, ...
    'C_PA', {[0.1 10]}, ...
    'R_SA', {[0.1 10]}, ...
    'R_tSA', {[0.5 15]}, ...
    'R_PA', {[0.1 10]}, ...
    'R_tPA', {[0.5 15]}, ...
    'R_t_o', {[0.1 10]}, ...
    'R_t_c', {[0.1 10]}, ...
    'R_p_o', {[0.1 10]}, ...
    'R_p_c', {[0.05 20]}, ...
    'R_m_o', {[0.1 10]}, ...
    'R_m_c', {[0.05 20]}, ...
    'R_a_o', {[0.1 10]}, ...
    'R_a_c', {[0.05 20]}, ...
    'k_pas_LV', {[0.1 10]}, ...
    'k_act_LV', {[0.1 10]}, ...
    'k_pas_RV', {[0.1 10]}, ...
    'k_act_RV', {[0.1 10]}, ...
    'Amref_LV', {[0.5 2]}, ...
    'Amref_RV', {[0.5 2]}, ...
    'Vw_LV', {[0.5 2]}, ...
    'Vw_RV', {[0.5 2]}, ...
    'R_Veins', {[0.05 20]}, ...
    'LvSepR',{[0.85 1.15]});
for i = 1:length(mods)
    bounds_i = pairs(mods{i});
    lb(i) = bounds_i{1}(1);
    ub(i) = bounds_i{1}(2);
end
end